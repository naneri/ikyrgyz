                                    <br/><br/>
                                    Песни, танцы и фольклор очень широко развиты у татар. Танцы обязательно сопровождались игрой на музыкальных инструментах. Основными из них считаются кубыза и курай.
                                    <br/><br/>
                                    Все танцы имеют классифицированные названия и предназначены для определенных мелодий. Особое значение имеет не только размер танца и акцент сильной доли, но и протяженность рисунка танца. Танец татар является очередным подтверждением наиболее ценного вида культуры одной из самых древних цивилизаций, которая и по сегодняшний день сохраняет свои генетические корни.
                                    <br/><br/>
                                    <img src="{{ URL::to('img/custom/tatar_culture_2.jpg') }}" style="width: 100%"/>
                                    <br/><br/>
                                    Нельзя не отметить разнообразную кухню татарского народа. Для традиционной татарской кухни характерен большой выбор мясных, молочных, постных супов и бульонов - токмач, умач, чумар, салма. Есть в татарской кухне и свои пищевые запреты. Так, по шариату запрещалось употреблять мясо свиньи, а также некоторых птиц, например, сокола, лебедя - последние считались священными.
                                    <br/><br/>
                                    Но особое место в кухне народа занимала разнообразная выпечка. Символом благополучия и достатка у татар служил хлеб - икмэк, который раньше пекли впрок 2-3 раза в неделю. Одно из самых древних печеных блюд - кыстыбый, представляющий собой сочень из пресного теста с начинкой из пшенной каши. Эчпочмак (треугольник) и перемячи - лепешки из дрожжевого или пресного теста с различной начинкой. Перемячи бывают открытыми или закрытыми, жарятся во фритюре или пекутся в духовке. Для праздничного стола готовилась губадия - круглый пирог с высокой многослойной начинкой, в которой всегда есть корт - красный сушеный творог.
