                                    <h2>Культура татар</h2>
                                    <br/>
                                    Татарами называют основное население Татарстана, религией которого является мусульманство. Культура этого народа довольно интересна и богата.
                                    <br/><br/>
                                    Традиционное одеяние татар отличается пошивом из материи домашнего изготовления, однако не есть исключением и покупной материал. Головным убором для мужчин служит тюбетейка, которая может быть различных форм. В холодное время года поверх неё надевалась стеганая шапка.
                                    <br/><br/>
                                    Праздники татар очень тесно связаны с их верой. Главными из них были: жертвоприношение – Корбан гаете, конец тридцатидневного поста – Ураза гаете, а также день рождения пророка Мухаммеда – Мэулид. Кроме того, очень значимыми обрядами и  праздниками были те, которые имели отношение к сельскохозяйственным работам. Праздник – сабантуй отмечался весной пред посевом. В этот день обычно устраивали соревнования по бегу, борьбе, прыжкам, конным скачкам, выжимание гири; бой мешками, наполненными соломой; разбивание горшков с закрытыми глазами; перетягивание каната или палки; хождение по наклонному бревну; влезание на верхушку столба; бег с ведрами, наполненными водой; бег с куринным яйцом; бег в мешке; парный бег двух человек с привязанными друг к другу ногами; доставание ртом монеты из катыка (кефир); ловля руками живой рыбы; ловля курицы; распил бревна и т.д.
                                    <br/><br/>
                                    Одним из самых популярных видом состязаний на сабантуях по-прежнему остается национальная борьба корэ́ш – борьба на поясах, в котором перед борцом стоит цель положить соперника на спину применением какого-либо из разрешенных приемов.
