                                    Свадьба у армян - это очень большой праздник. Ее церемониал включает сговор, обручение и само свадебное торжество. Сегодня уже изжила себя традиция гулять, как говорится, «семь дней, семь ночей». Хотя раньше гуляли именно так. В роли свидетелей на армянской свадьбе выступают «крестные свадьбы». Как правило, это самая близкая, самая уважаемая семейная пара из окружения жениха и невесты. Во время выкупа невесты родные могут назначить за нее любую сумму, и дело чести крестного найти эти деньги, хотя, как правило, суммы чисто символические. Невесту ведет к алтарю – посаженный отец, а крестные несут ответственность за семью с начала ее образования и до самого конца.
