                                    <h2>Культура Армян</h2>
                                    <br/>
                                    Термин «Арабская культура» иногда распространяют и на все те культуры, которые создавались в средние века, как арабскими народами, так и народами Ближнего и Среднего Востока, Северной Африки и Юго-Западной Европы, находившихся тогда под властью или под непосредственным влиянием арабского халифата.
                                    <br/><br/>
                                    Художественную арабскую литературу в первый, доисламский период (V — начало VII века) творили кочевые племена арабов, а позднее и арабизированные народы. . Сначала возникла проза — легенды, рассказы, позже — поэзия; переходом к стихотворению была рифмованная проза — «садж».
                                    <br/><br/>
                                    В испанском городе Гранаде сохранился роскошный средневековый дворец Альгамбра (с араб. — красный). О происхождении его названия повествует дав¬няя легенда, согласно которой строители возводили дворец непрерывно, днём и ночью, разжигая огромные костры, пламя которых отражалось на его стенах. Он был построен в XIII-XIV вв. и является украшением мусульманской архитектуры. Правители Гранады пытались придать Альгамбре настоящую восточную роскошь, ослепить её блеском весь мир. Дворец окружён зеленью садов, среди которых свер¬кают жемчужными струями фонтаны и играют солнечными лучами прозрачные бассейны. На территории Альгамбры расположено немало внутренних двориков для отдыха, а также для гостей и послов.
