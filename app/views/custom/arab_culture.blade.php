                                    Древнейшие памятники относятся к эпохе рабовладельческого общества. В VII—X веках оно ещё наследует эллинистическое, коптское, византийское и сасанидское искусство. Но уже в это время развивается прикладное искусство: появляются художественные ткани с тонким узором, изделия из бронзы, горного хрусталя, керамика с цветными поливами, стекло, резьба по дереву.
                                    <br/><br/>
                                    К II—V векам относятся остатки монументальных сводчатых сооружений в Хаурани (Сирия). На ранних памятниках арабской архитектуры сказалось влияние эллинистическо-римской, византийской и сасанидской традиций, например дворец IV—VIII веков в Мшатти (Иордания), мечеть «Купол Скалы» (691) в Иерусалиме (Палестина).
                                    <br/><br/>
                                    Поражают успехи арабов в медицине и астроно¬мии. Арабские учебники по медицине были достаточно популярными в странах Западной Европы. Светилом средневековой медицины считают Ибн-Сину (ок. 980-1037), уроженца небольшого селения вблизи Бухары.
                                    <br/><br/>
                                    Арабский танец относится к семейству восточных танцев  и является одним из наиболее распространенных и популярных танцев во многих странах Востока. Его исполнение можно увидеть повсеместно в Северной Африке и на Ближнем Востоке. Арабский танец оказывает благоприятное влияние на здоровье и эмоциональное состояние женского организма в целом. По мнению специалистов, что по мере освоения элементов арабского танца изменяется самоощущение женщины, исчезает комплекс неполноценности и повышается самооценка. Все это помогает женщине познать себя, раскрыть свою женственность, и при этом укрепить здоровье.
