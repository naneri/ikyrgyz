                                    <br/><br/>
                                    Свадьба является наиболее ярким торжеством, которое наряду с частным имеет и социальный характер. Свадьбе, как правило, предшествует сватовство. Сватовство совершается после обоюдного решения молодых вступить в брак. Ныне иногда встречается форма брака путем умыкания невесты «кыз апкечиш». Это явление связанно с несогласием родителей невесты на брак. Но как часто бывает в жизни, родители вскоре приходят к соглашению и проводят свадьбу.
                                    <br/><br/>
                                    Основным момента любой свадьбы является сам обрядом бракосочетания – «нека». Если раньше присутствие невесты считалось необязательным, то сейчас молодые сами повсеместно принимают участие в этом обряде.
