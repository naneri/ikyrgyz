                                    <br/><br/>
                                    Важным моментом в жизни семьи является рождение ребенка. Современные уйгуры исполняют все обряды, связанные с рождением, воспитанием ребенка. Первого ребенка, женщина согласно обычаю, рожает в доме матери. За 20-30 дней до родов мать молодой идет в дом зятя и просит разрешения – «тилэп елиш» - забрать роженицу к себе. Мать сопровождают родственницы и близкие с подарками. Доступ к роженице ограничен в течении всего периода. На 12-й день происходит имянаречение, для чего приглашаются родственники мужа и мулла. На сороковой день проводится обряд купания ребенка – «кирик суйи». Для этого в одну кастрюлю кладут щепотку соли, адрасман, 1-2 куска сахара рафинада, монеты. Рождение следующих детей и их «кирик суйи» происходит в доме отца ребенка.
                                    <br/><br/>
                                    Весьма торжественно обставляется обрезание мальчиков – «суннэт той». Обрезание совершается в возрасте 5-7 лет. Обычно пышное торжество устраиватется лишь для первого сына.
