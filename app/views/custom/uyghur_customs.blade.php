                                    <h2>Обычаи уйгуров</h2>
                                    <br/>
                                    Мяшряп - старинный обычай, которого придерживаются уйгуры, иначе это – вечер отдыха. Он начинается поздней осенью и продолжается до весны, собирая в своем кругу мужчин одного возраста, проживающих в одном районе или селе, имеющих общие интересы. Участников таких собраний называют «тридцать джигитов» - «оттуз огул».  «Мяшряп» - это школа этического и эстетического воспитания. Он выявляет людей одаренных музыкальными или поэтическими способностями, также он является школой воспитания дисциплины и ответственности перед коллективом.
                                    <br/><br/>
